#include <iostream>
using namespace std;

class Person_2_8 {
public:
	//无参（默认）构造函数
	Person_2_8() {
		cout << "无参构造函数!" << endl;
	}
	//有参构造函数
	Person_2_8(int a) {
		age = a;
		cout << "有参构造函数!" << endl;
	}
	//拷贝构造函数
	Person_2_8(const Person_2_8& p) {
		age = p.age;
		cout << "拷贝构造函数!" << endl;
	}
	//析构函数
	~Person_2_8() {
		cout << "析构函数!" << endl;
	}
public:
	int age;
};


//2、构造函数的调用
//调用无参构造函数
void test01_2_8() {
	Person_2_8 p; //调用无参构造函数
}

//调用有参的构造函数
void test02_2_8() {

	//2.1  括号法，常用
	Person_2_8 p1(10);
	//注意1：调用无参构造函数不能加括号，如果加了编译器认为这是一个函数声明
	//Person p2();

	//2.2 显式法
	Person_2_8 p2 = Person_2_8(10);
	Person_2_8 p3 = Person_2_8(p2);
	//Person(10)单独写就是匿名对象  当前行结束之后，马上析构

	//2.3 隐式转换法
	Person_2_8 p4 = 10; // Person p4 = Person(10); 
	Person_2_8 p5 = p4; // Person p5 = Person(p4); 

	//注意2：不能利用 拷贝构造函数 初始化匿名对象 编译器认为是对象声明
	//Person p5(p4);
}

